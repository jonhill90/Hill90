// Default fallback: minimal circle-dot
const DEFAULT_PATH = (
  <>
    <circle cx="12" cy="12" r="10" fill="none" stroke="currentColor" strokeWidth="1.5" />
    <circle cx="12" cy="12" r="3" />
  </>
)

export function getProviderPath(provider: string): React.ReactNode {
  return PROVIDER_PATHS[provider] || DEFAULT_PATH
}

/** Single provider icon — inline SVG, inherits color from parent */
export function ProviderIcon({ provider, className }: { provider: string; className?: string }) {
  const isFallback = !(provider in PROVIDER_PATHS)
  return (
    <svg
      viewBox="0 0 24 24"
      fill="currentColor"
      className={className ?? 'h-6 w-6'}
      aria-label={provider || 'provider'}
      data-testid={`provider-icon-${provider || 'unknown'}`}
      data-fallback={isFallback}
    >
      {getProviderPath(provider)}
    </svg>
  )
}

/** Composite icon for router models with multiple providers */
export function CompositeProviderIcon({ providers, className }: { providers: string[]; className?: string }) {
  const unique = [...new Set(providers)]
  if (unique.length === 0) return <ProviderIcon provider="" className={className} />
  if (unique.length === 1) return <ProviderIcon provider={unique[0]} className={className} />

  if (unique.length === 2) {
    return (
      <div className={`flex items-center -space-x-1 ${className ?? 'h-6 w-6'}`} data-testid="composite-provider-icon">
        <ProviderIcon provider={unique[0]} className="h-4 w-4" />
        <ProviderIcon provider={unique[1]} className="h-4 w-4" />
      </div>
    )
  }

  // 3+ providers: compact 2x2 grid
  const shown = unique.slice(0, 4)
  const overflow = unique.length - 4
  return (
    <div className={`grid grid-cols-2 gap-px ${className ?? 'h-6 w-6'}`} data-testid="composite-provider-icon">
      {shown.map(p => (
        <ProviderIcon key={p} provider={p} className="h-3 w-3" />
      ))}
      {overflow > 0 && (
        <span className="text-[8px] text-mountain-500 flex items-center justify-center">
          +{overflow}
        </span>
      )}
    </div>
  )
}
